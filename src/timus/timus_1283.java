package timus;

//1283. Гномик
//        Ограничение времени: 1.0 секунды
//        Ограничение памяти: 64 МБ
//        Венерианские гномики — мелочные, уродливые, противные, злобные создания. Говорят, что такими их сделали тяжёлые условия жизни на их планете… но факт остается фактом: в молодости каждый такой гад готов продать мать родную (и это отнюдь не эвфемизм!), чтобы скопить свой горшочек с золотом, а потом, набив этот горшок доверху, каждый гномик трясётся над ним до конца дней своих.
//        Особые волнения гномикам доставляют меркурианские леприконсы, — те ещё поганцы — которые всегда рады улучить момент для того, чтобы опустошить гномий горшочек и набить его вместо золота солнечной пылью (на Меркурии её, сами понимаете, как у нас картошки). Гномики подслеповаты и пыль от золота отличить не могут. Поэтому каждый гномик раз в год наведывается в Центральный Галактический Банк (ЦГБ), где опытные эксперты за небольшую комиссию проверяют золото на подлинность.
//        Когда у гномика остаётся золота меньше либо равно определённому количеству, жизнь его теряет всякий смысл, и он освобождает мир от своей жалкой душонки весьма оригинальным способом: на оставшееся золото он покупает в зоопарке самую большую юпитерианскую жабу и забирается под неё. Смерть наступает в результате деформации грудной клетки.
//        Исходные данные
//        На входе три целых числа через пробел. Первое число — количество золота в гномьем горшочке в начальный момент времени. Второе число — количество золота, при котором жизнь гномика лишается смысла. Обе величины не превышают 231 − 1. Третье число — комиссия ЦГБ в процентах (лежит в пределах от 1 до 99).
//        Результат
//        Следует вывести единственное число — сколько лет осталось жить гномику.
//        Примеры
//        исходные данные	результат
//        19 10 50
//        1
//        1000 1 1
//        688

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class timus_1283 {
    public static void main(String[] args) throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        String[] parts = reader.readLine().trim().split("\\s+");
        double gold = Double.parseDouble(parts[0]);      // начальное количество золота
        double threshold = Double.parseDouble(parts[1]);   // порог, при котором гномик "умирает"
        int commission = Integer.parseInt(parts[2]);       // комиссия в процентах

        int years = 0;
        while (gold > threshold) {
            gold = gold * (100 - commission) / 100.0;
            years++;
        }

        System.out.println(years);
    }
}